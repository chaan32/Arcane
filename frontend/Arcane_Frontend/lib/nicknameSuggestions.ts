const randomItem = (items: string[]) =>
  items[Math.floor(Math.random() * items.length)];

const randomNumberSuffix = () =>
  String(Math.floor(Math.random() * 1000)).padStart(3, "0");

export const generateNicknameSuggestion = () =>
  `${randomItem(ADJECTIVES)}${randomItem(ANIMALS)}${randomNumberSuffix()}`;

export const generateNicknameSuggestions = (count = 3) => {
  const suggestions = new Set<string>();

  while (suggestions.size < count) {
    suggestions.add(generateNicknameSuggestion());
  }

  return Array.from(suggestions);
};
